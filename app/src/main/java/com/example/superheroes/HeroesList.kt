/**
 * Composable that uses HeroItem composable to generate a list of heroes
 *
 * @param heroes list of heroes
 * @param modifier modifier modifiers to set to this composable
 */
@OptIn(ExperimentalAnimationApi::class)
@Composable
fun HeroesList(
    heroes: List<Hero>,
    modifier: Modifier = Modifier,
    contentPadding: PaddingValues = PaddingValues(0.dp)
) {
    val visibleState = remember {
        MutableTransitionState(false).apply {
            targetState = true
        }
    }

    AnimatedVisibility(
        visibleState = visibleState,
        enter = fadeIn(
            animationSpec = spring(DampingRatioLowBouncy)
        ),
        exit = fadeOut(),
        modifier = modifier
    ) {
        LazyColumn(contentPadding =  contentPadding) {
            itemsIndexed(heroes) { index, hero ->
                HeroItem(
                    hero,
                    modifier = Modifier
                        .padding(
                            horizontal = dimensionResource(R.dimen.padding_medium),
                            vertical = dimensionResource(R.dimen.padding_medium),
                        )
                        .animateEnterExit(
                            enter = slideInVertically(
                                animationSpec = spring(
                                    stiffness = StiffnessVeryLow,
                                    dampingRatio = DampingRatioLowBouncy
                                ),
                                initialOffsetY = { it * (index + 1) } // staggered entrance
                            )
                        )
                )
            }
        }
    }
}

@Composable
@Preview
fun HeroesListPreview() {
    SuperheroesTheme(darkTheme = true) {
        Surface(color = MaterialTheme.colorScheme.background) {
            HeroesList(HeroesRepository.heroes)
        }
    }
}